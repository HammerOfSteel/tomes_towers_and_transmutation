#!/usr/bin/env node
/**
 * scripts/gen-manifest.mjs
 *
 * Scans public/assets/ for every *.glb file and writes
 * src/assetManifest.ts — imported by the sandbox at build time so
 * every tile is always available without manual maintenance.
 *
 * Usage:
 *   node scripts/gen-manifest.mjs
 *
 * Add to package.json scripts:
 *   "manifest": "node scripts/gen-manifest.mjs"
 */

import { readdirSync, writeFileSync, statSync } from 'fs';
import { join, relative } from 'path';
import { fileURLToPath } from 'url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const ROOT      = join(__dirname, '..');
const ASSET_DIR = join(ROOT, 'public', 'assets');
const OUT_FILE  = join(ROOT, 'src', 'assetManifest.ts');

/** Recursively collect *.glb paths relative to `public/` */
function walkGlb(dir, base = dir) {
  const results = [];
  for (const entry of readdirSync(dir).sort()) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      results.push(...walkGlb(full, base));
    } else if (entry.endsWith('.glb')) {
      // Convert to URL path relative to public/  e.g. /assets/buildings/wall.glb
      results.push('/' + relative(join(ROOT, 'public'), full).replace(/\\/g, '/'));
    }
  }
  return results;
}

const packs = {};
for (const packName of readdirSync(ASSET_DIR).sort()) {
  const full = join(ASSET_DIR, packName);
  if (!statSync(full).isDirectory()) continue;
  const files = walkGlb(full, ASSET_DIR);
  if (files.length > 0) packs[packName] = files;
}

const totalGlbs = Object.values(packs).reduce((s, a) => s + a.length, 0);

const header = `\
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// AUTO-GENERATED by scripts/gen-manifest.mjs
// DO NOT EDIT — run  node scripts/gen-manifest.mjs  to refresh
// ${totalGlbs} GLBs across ${Object.keys(packs).length} asset packs
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

`;

const body = `export const ASSET_PACKS: Record<string, string[]> = ` +
  JSON.stringify(packs, null, 2) + ';\n';

writeFileSync(OUT_FILE, header + body, 'utf8');

console.log(`✓ Wrote ${OUT_FILE}`);
console.log(`  ${totalGlbs} GLBs across ${Object.keys(packs).length} packs:`);
for (const [name, files] of Object.entries(packs)) {
  console.log(`    ${name.padEnd(12)} ${files.length} tiles`);
}
